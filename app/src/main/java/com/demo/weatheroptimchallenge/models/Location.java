public class Location {

    String country;
    String state;
    String city;
    String latitude;
    String longitude;
    String zip;

    public String getCountry() {
        return country;
    }

    public String getState() {
        return state;
    }

    public String getCity() {
        return city;
    }

    public String getLatitude() {
        return latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public String getZip() {
        return zip;
    }
}
